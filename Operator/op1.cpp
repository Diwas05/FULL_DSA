#include <iostream>
using namespace std;
int main(){
    int i=7;
    cout<<i++<<endl;
    //print->7 and then increment the i value to 8 
    cout<<++i<<endl;
    //First will increment the i value to 9 and print->9
    cout<<i--<<endl;
    //print->9 and decrement the i to 8
    cout<<--i<<endl;
    //First will decrement the i to 7 and print->7
}